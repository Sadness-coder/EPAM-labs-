public class Calculator {
    public static float addition(int firstNumber, int secondNumber){
        return firstNumber + secondNumber;
    }

    public static float substraction(int firstNumber, int secondNumber){
        return firstNumber - secondNumber;
    }

    public static float multiplication(int firstNumber, int secondNumber){
        return firstNumber * secondNumber;
    }

    public static int division(int firstNumber, int secondNumber){
        return firstNumber/secondNumber;
    }
}
